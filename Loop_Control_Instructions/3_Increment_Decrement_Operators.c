
// Chapter 4 (Loop Control Instructions) - Task 3: Pre and Post Increment/Decrement
#include <stdio.h>
int main(){
    int i = 5;
    printf("i is %d\n", i);
    i = i+5;
    printf("i is %d\n", i);
    printf("i is %d\n", i++);
    printf("i is %d\n", ++i);
    printf("i is %d\n", i--);
    printf("i is %d\n", i);
    return 0;
}
