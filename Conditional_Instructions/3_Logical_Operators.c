
// Chapter 3 (Conditional Instructions) - Task 3: Logical Operators
#include <stdio.h>
int main(){
    int a = 1, b = 0;
    printf("The value of a and b is %d\n", a&&b);
    printf("The value of a or b is %d\n", a||b);
    printf("The value of !a is %d\n", !a);
    printf("The value of !b is %d\n", !b);
    return 0;
}
